# Interpolation

x = 10
y = 20
puts "#{x} + #{y} = #{x + y}"
10 + 20 = 30


puts "100 * 5 = #{100 * 5}"
100 * 5 = 500


x = "cat"
puts "The #{x} in the hat"
The cat in the hat


puts "It's a #{"bad " * 5}world"
It's a bad bad bad bad bad world


x = 10
y = 20
puts x.to_s + " + " + y.to_s + " = " + (x + y).to_s
puts "#{x} + #{y} = #{x + y}"
